-- Habilitar la resaltación de la línea del cursor
vim.opt.cursorline = true

-- Habilitar colores de terminal de 24 bits
vim.opt.termguicolors = true

-- Configurar la opacidad de las ventanas flotantes a 0 (sin opacidad)
vim.opt.winblend = 0

-- Configurar las opciones de autocompletado de comandos
vim.opt.wildoptions = 'pum'

-- Establecer la opacidad de la ventana emergente del autocompletado a 5
vim.opt.pumblend = 5

-- Establecer el fondo del entorno Vim a 'dark' (oscuro)
vim.opt.background = 'dark'
